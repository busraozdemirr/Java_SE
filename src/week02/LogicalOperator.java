package week02;

public class LogicalOperator {
    public static void main(String[] args) {
        String username = "busraozdemir";
        int password = 123456;
        System.out.println(username.equals("busraozdemir"));
        System.out.println(username.equals("busra"));
        System.out.println(password == 123456);
        System.out.println(password == 0123456);

    }
}
